package Pr2;

public class SpecialCharEx {		//클래스 선언

	public static void main(String[] args) {
		System.out.println('\'');				//문자형태인 '를 출력
		System.out.println("abc\t123\b456");	//abc 탭 123 특수문자 456를 출력
		System.out.println('\n');				//줄바꿈 출력
		System.out.println("\"Hello\"");		//문자열 Hello와 문자형태인 "를 문자앞뒤로 출력
		System.out.println("c:\\");				//문자c와 문자형태인 \출력

	}

}
